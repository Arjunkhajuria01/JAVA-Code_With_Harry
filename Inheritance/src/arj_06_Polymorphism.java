//POLYMORPHISM
/*
    The word polymorphism means "many forms".
    In Java OOP, it means the same method name (or operator) can perform different tasks depending on the context.
    There are two main types:
    Compile-time polymorphism (Static polymorphism) → achieved by method overloading.
    Runtime polymorphism (Dynamic polymorphism) → achieved by method overriding.

 */
/*
    2. Compile-Time Polymorphism (Method Overloading)
    When multiple methods have the same name but different parameter lists, the
    compiler decides which one to call at compile time.
 */
/*
    3. Runtime Polymorphism (Method Overriding)
    When a subclass provides its own implementation of a method that already exists in the parent class,
    this is overriding.
    Here, the method call is resolved at runtime, depending on the object type.
 */
/*
    4. Why Use Polymorphism?
    Code reusability – Same method name, different behaviors.
    Flexibility – You can write general code for the parent class and let subclasses decide behavior.
    Maintainability – Easy to extend (just add new classes with overridden methods).
 */












